<h2>Sobre o Projeto</h2>
<p>Este site foi criado para oferecer acesso gratuito a uma vasta coleção de livros digitais de domínio público,
    permitindo que qualquer pessoa possa explorar e ler obras literárias sem restrições. Com uma interface simples e
    intuitiva, os usuários podem buscar livros por título, autor ou gênero, facilitando a descoberta de novos conteúdos.
    A plataforma é acessível de qualquer dispositivo, garantindo uma experiência de leitura fluida e agradável.

    Nosso objetivo é promover a disseminação do conhecimento e da cultura, preservando obras clássicas e tornando-as
    disponíveis para todos. Todos os livros disponíveis são cuidadosamente organizados e estão livres de direitos
    autorais, permitindo seu uso sem restrições.</p>